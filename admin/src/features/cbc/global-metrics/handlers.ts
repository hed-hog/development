import { useDefaultMutation } from "@/hooks/use-default-mutation";
import { useQuery } from "@tanstack/react-query";
import { requests } from "./requests";

const scope = "global-metrics";

export function useGlobalMetricsCreate() {
  const { globalMetricsCreate } = requests();
  return useDefaultMutation(scope, "create", globalMetricsCreate);
}

export function useGlobalMetricsDelete() {
  const { globalMetricsDelete } = requests();
  return useDefaultMutation(scope, "delete", globalMetricsDelete);
}

export function useGlobalMetricsUpdate() {
  const { globalMetricsUpdate } = requests();
  return useDefaultMutation(scope, "update", globalMetricsUpdate);
}

export function useGlobalMetricsGet(id: number) {
  const { globalMetricsGet } = requests();
  return useQuery({
    queryKey: [scope, "get"],
    queryFn: () => globalMetricsGet(id),
  });
}
