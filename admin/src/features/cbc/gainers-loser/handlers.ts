import { useDefaultMutation } from "@/hooks/use-default-mutation";
import { useQuery } from "@tanstack/react-query";
import { requests } from "./requests";

const scope = "gainers-loser";

export function useGainersLoserCreate() {
  const { gainersLoserCreate } = requests();
  return useDefaultMutation(scope, "create", gainersLoserCreate);
}

export function useGainersLoserDelete() {
  const { gainersLoserDelete } = requests();
  return useDefaultMutation(scope, "delete", gainersLoserDelete);
}

export function useGainersLoserUpdate() {
  const { gainersLoserUpdate } = requests();
  return useDefaultMutation(scope, "update", gainersLoserUpdate);
}

export function useGainersLoserGet(id: number) {
  const { gainersLoserGet } = requests();
  return useQuery({
    queryKey: [scope, "get"],
    queryFn: () => gainersLoserGet(id),
  });
}
