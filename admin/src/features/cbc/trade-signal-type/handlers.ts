import { useDefaultMutation } from "@/hooks/use-default-mutation";
import { useQuery } from "@tanstack/react-query";
import { requests } from "./requests";

const scope = "trade-signal-type";

export function useTradeSignalTypeCreate() {
  const { tradeSignalTypeCreate } = requests();
  return useDefaultMutation(scope, "create", tradeSignalTypeCreate);
}

export function useTradeSignalTypeDelete() {
  const { tradeSignalTypeDelete } = requests();
  return useDefaultMutation(scope, "delete", tradeSignalTypeDelete);
}

export function useTradeSignalTypeUpdate() {
  const { tradeSignalTypeUpdate } = requests();
  return useDefaultMutation(scope, "update", tradeSignalTypeUpdate);
}

export function useTradeSignalTypeGet(id: number) {
  const { tradeSignalTypeGet } = requests();
  return useQuery({
    queryKey: [scope, "get"],
    queryFn: () => tradeSignalTypeGet(id),
  });
}
