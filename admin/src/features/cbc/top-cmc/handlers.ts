import { useDefaultMutation } from "@/hooks/use-default-mutation";
import { useQuery } from "@tanstack/react-query";
import { requests } from "./requests";

const scope = "top-cmc";

export function useTopCmcCreate() {
  const { topCmcCreate } = requests();
  return useDefaultMutation(scope, "create", topCmcCreate);
}

export function useTopCmcDelete() {
  const { topCmcDelete } = requests();
  return useDefaultMutation(scope, "delete", topCmcDelete);
}

export function useTopCmcUpdate() {
  const { topCmcUpdate } = requests();
  return useDefaultMutation(scope, "update", topCmcUpdate);
}

export function useTopCmcGet(id: number) {
  const { topCmcGet } = requests();
  return useQuery({
    queryKey: [scope, "get"],
    queryFn: () => topCmcGet(id),
  });
}
