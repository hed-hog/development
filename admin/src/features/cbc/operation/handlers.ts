import { useDefaultMutation } from "@/hooks/use-default-mutation";
import { useQuery } from "@tanstack/react-query";
import { requests } from "./requests";

const scope = "operation";

export function useOperationCreate() {
  const { operationCreate } = requests();
  return useDefaultMutation(scope, "create", operationCreate);
}

export function useOperationDelete() {
  const { operationDelete } = requests();
  return useDefaultMutation(scope, "delete", operationDelete);
}

export function useOperationUpdate() {
  const { operationUpdate } = requests();
  return useDefaultMutation(scope, "update", operationUpdate);
}

export function useOperationGet(id: number) {
  const { operationGet } = requests();
  return useQuery({
    queryKey: [scope, "get"],
    queryFn: () => operationGet(id),
  });
}
