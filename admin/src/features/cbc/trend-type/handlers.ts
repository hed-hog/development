import { useDefaultMutation } from "@/hooks/use-default-mutation";
import { useQuery } from "@tanstack/react-query";
import { requests } from "./requests";

const scope = "trend-type";

export function useTrendTypeCreate() {
  const { trendTypeCreate } = requests();
  return useDefaultMutation(scope, "create", trendTypeCreate);
}

export function useTrendTypeDelete() {
  const { trendTypeDelete } = requests();
  return useDefaultMutation(scope, "delete", trendTypeDelete);
}

export function useTrendTypeUpdate() {
  const { trendTypeUpdate } = requests();
  return useDefaultMutation(scope, "update", trendTypeUpdate);
}

export function useTrendTypeGet(id: number) {
  const { trendTypeGet } = requests();
  return useQuery({
    queryKey: [scope, "get"],
    queryFn: () => trendTypeGet(id),
  });
}
