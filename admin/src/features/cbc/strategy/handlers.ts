import { useDefaultMutation } from "@/hooks/use-default-mutation";
import { useQuery } from "@tanstack/react-query";
import { requests } from "./requests";

const scope = "strategy";

export function useStrategyCreate() {
  const { strategyCreate } = requests();
  return useDefaultMutation(scope, "create", strategyCreate);
}

export function useStrategyDelete() {
  const { strategyDelete } = requests();
  return useDefaultMutation(scope, "delete", strategyDelete);
}

export function useStrategyUpdate() {
  const { strategyUpdate } = requests();
  return useDefaultMutation(scope, "update", strategyUpdate);
}

export function useStrategyGet(id: number) {
  const { strategyGet } = requests();
  return useQuery({
    queryKey: [scope, "get"],
    queryFn: () => strategyGet(id),
  });
}
