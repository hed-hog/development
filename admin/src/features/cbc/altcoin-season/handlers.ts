import { useDefaultMutation } from "@/hooks/use-default-mutation";
import { useQuery } from "@tanstack/react-query";
import { requests } from "./requests";

const scope = "altcoin-season";

export function useAltcoinSeasonCreate() {
  const { altcoinSeasonCreate } = requests();
  return useDefaultMutation(scope, "create", altcoinSeasonCreate);
}

export function useAltcoinSeasonDelete() {
  const { altcoinSeasonDelete } = requests();
  return useDefaultMutation(scope, "delete", altcoinSeasonDelete);
}

export function useAltcoinSeasonUpdate() {
  const { altcoinSeasonUpdate } = requests();
  return useDefaultMutation(scope, "update", altcoinSeasonUpdate);
}

export function useAltcoinSeasonGet(id: number) {
  const { altcoinSeasonGet } = requests();
  return useQuery({
    queryKey: [scope, "get"],
    queryFn: () => altcoinSeasonGet(id),
  });
}
