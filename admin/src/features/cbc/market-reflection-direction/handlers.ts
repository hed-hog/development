import { useDefaultMutation } from "@/hooks/use-default-mutation";
import { useQuery } from "@tanstack/react-query";
import { requests } from "./requests";

const scope = "market-reflection-direction";

export function useMarketReflectionDirectionCreate() {
  const { marketReflectionDirectionCreate } = requests();
  return useDefaultMutation(scope, "create", marketReflectionDirectionCreate);
}

export function useMarketReflectionDirectionDelete() {
  const { marketReflectionDirectionDelete } = requests();
  return useDefaultMutation(scope, "delete", marketReflectionDirectionDelete);
}

export function useMarketReflectionDirectionUpdate() {
  const { marketReflectionDirectionUpdate } = requests();
  return useDefaultMutation(scope, "update", marketReflectionDirectionUpdate);
}

export function useMarketReflectionDirectionGet(id: number) {
  const { marketReflectionDirectionGet } = requests();
  return useQuery({
    queryKey: [scope, "get"],
    queryFn: () => marketReflectionDirectionGet(id),
  });
}
