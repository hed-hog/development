import { useDefaultMutation } from "@/hooks/use-default-mutation";
import { useQuery } from "@tanstack/react-query";
import { requests } from "./requests";

const scope = "stock-exchange";

export function useStockExchangeCreate() {
  const { stockExchangeCreate } = requests();
  return useDefaultMutation(scope, "create", stockExchangeCreate);
}

export function useStockExchangeDelete() {
  const { stockExchangeDelete } = requests();
  return useDefaultMutation(scope, "delete", stockExchangeDelete);
}

export function useStockExchangeUpdate() {
  const { stockExchangeUpdate } = requests();
  return useDefaultMutation(scope, "update", stockExchangeUpdate);
}

export function useStockExchangeGet(id: number) {
  const { stockExchangeGet } = requests();
  return useQuery({
    queryKey: [scope, "get"],
    queryFn: () => stockExchangeGet(id),
  });
}
