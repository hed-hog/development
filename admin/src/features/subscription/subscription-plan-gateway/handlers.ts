import { useDefaultMutation } from "@/hooks/use-default-mutation";
import { useQuery } from "@tanstack/react-query";
import { requests } from "./requests";

const scope = "subscription-plan-gateway";

export function useSubscriptionPlanGatewayCreate() {
  const { subscriptionPlanGatewayCreate } = requests();
  return useDefaultMutation(scope, "create", subscriptionPlanGatewayCreate);
}

export function useSubscriptionPlanGatewayDelete() {
  const { subscriptionPlanGatewayDelete } = requests();
  return useDefaultMutation(scope, "delete", subscriptionPlanGatewayDelete);
}

export function useSubscriptionPlanGatewayUpdate() {
  const { subscriptionPlanGatewayUpdate } = requests();
  return useDefaultMutation(scope, "update", subscriptionPlanGatewayUpdate);
}

export function useSubscriptionPlanGatewayGet(planId: number, id: number) {
  const { subscriptionPlanGatewayGet } = requests();
  return useQuery({
    queryKey: [scope, "get"],
    queryFn: () => subscriptionPlanGatewayGet({ planId, id }),
  });
}
