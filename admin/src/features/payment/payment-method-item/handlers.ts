import { useDefaultMutation } from "@/hooks/use-default-mutation";
import { useQuery } from "@tanstack/react-query";
import { requests } from "./requests";

const scope = "payment-method-item";

export function usePaymentMethodItemCreate() {
  const { paymentMethodItemCreate } = requests();
  return useDefaultMutation(scope, "create", paymentMethodItemCreate);
}

export function usePaymentMethodItemDelete() {
  const { paymentMethodItemDelete } = requests();
  return useDefaultMutation(scope, "delete", paymentMethodItemDelete);
}

export function usePaymentMethodItemUpdate() {
  const { paymentMethodItemUpdate } = requests();
  return useDefaultMutation(scope, "update", paymentMethodItemUpdate);
}

export function usePaymentMethodItemGet(id: number) {
  const { paymentMethodItemGet } = requests();
  return useQuery({
    queryKey: [scope, "get"],
    queryFn: () => paymentMethodItemGet(id),
  });
}
