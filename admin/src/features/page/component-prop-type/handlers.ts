import { useDefaultMutation } from "@/hooks/use-default-mutation";
import { useQuery } from "@tanstack/react-query";
import { requests } from "./requests";

const scope = "component-prop-type";

export function useComponentPropTypeCreate() {
  const { componentPropTypeCreate } = requests();
  return useDefaultMutation(scope, "create", componentPropTypeCreate);
}

export function useComponentPropTypeDelete() {
  const { componentPropTypeDelete } = requests();
  return useDefaultMutation(scope, "delete", componentPropTypeDelete);
}

export function useComponentPropTypeUpdate() {
  const { componentPropTypeUpdate } = requests();
  return useDefaultMutation(scope, "update", componentPropTypeUpdate);
}

export function useComponentPropTypeGet(id: number) {
  const { componentPropTypeGet } = requests();
  return useQuery({
    queryKey: [scope, "get"],
    queryFn: () => componentPropTypeGet(id),
  });
}
