import { useDefaultMutation } from "@/hooks/use-default-mutation";
import { useQuery } from "@tanstack/react-query";
import { requests } from "./requests";

const scope = "component-type";

export function useComponentTypeCreate() {
  const { componentTypeCreate } = requests();
  return useDefaultMutation(scope, "create", componentTypeCreate);
}

export function useComponentTypeDelete() {
  const { componentTypeDelete } = requests();
  return useDefaultMutation(scope, "delete", componentTypeDelete);
}

export function useComponentTypeUpdate() {
  const { componentTypeUpdate } = requests();
  return useDefaultMutation(scope, "update", componentTypeUpdate);
}

export function useComponentTypeGet(id: number) {
  const { componentTypeGet } = requests();
  return useQuery({
    queryKey: [scope, "get"],
    queryFn: () => componentTypeGet(id),
  });
}
